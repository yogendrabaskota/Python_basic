x = 100
y = 20 

print("sum of x and y is ", x+y)
print("Differencve is ", x-y)
print("division is ", x/y)
print("multiplication between", x, "and", y, "is", x*y)
print(x ** y)  # output will be very largeeee
print(x // y)  # Floor division: 5

#COMPARISON OPERATOR
print(x == y) #false
print(x > y) 
print(x < y)
print(x != y)
print(x >= y)
print(x <= y)

#LOGICAL OPERATOR
a = True
b = False
print(a and b) #false
print(a or b) #true
print(not b) #true


#ASSIGNMENT OPERATOR
p = 5
print(p) # p=5
p+=5
print(p)  #p=p+5=5+5=10
p*=6
print(p) #p=p*6=10*6=60


#BITWISE OPERATOR
a = 5  # Binary: 101
b = 3  # Binary: 011
print(a & b)  # Bitwise AND: 1 (Binary: 001)
print(a | b)  # Bitwise OR: 7 (Binary: 111)
print(a ^ b)  # Bitwise XOR: 6 (Binary: 110)
print(~a)     # Bitwise NOT: -6 (Binary: -110)
print(a << 1)  # Bitwise left shift: 10 (Binary: 1010)
print(a >> 1)  # Bitwise right shift: 2 (Binary: 010)


